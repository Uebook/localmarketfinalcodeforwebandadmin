// Helper functions
export const getAllStates = () => {
  return Object.keys(INDIAN_STATES_CITIES).sort();
};

export const getCitiesByState = (state) => {
  if (!state || !INDIAN_STATES_CITIES[state]) return [];
  return INDIAN_STATES_CITIES[state].sort();
};

// Search functions
export const searchStates = (query) => {
  const states = getAllStates();
  if (!query) return states;
  const lowerQuery = query.toLowerCase();
  return states.filter(state => state.toLowerCase().includes(lowerQuery));
};

export const searchCities = (state, query) => {
  const cities = getCitiesByState(state);
  if (!query) return cities;
  const lowerQuery = query.toLowerCase();
  return cities.filter(city => city.toLowerCase().includes(lowerQuery));
};
